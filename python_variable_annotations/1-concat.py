#!/usr/bin/env python3
"""Module for concatenating two strings."""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the result.

    Args:
        str1: First string
        str2: Second string

    Returns:
        Concatenation of str1 and str2 as a string
    """
    return str1 + str2
